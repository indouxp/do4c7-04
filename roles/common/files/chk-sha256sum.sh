#!/bin/sh
###############################################################################
#
# FILEのsha256sumを取得し、STRと一致しなければ、エラー
#
###############################################################################
FILE=$1
STR=$2

[ ! -x `which sha256sum` ]  && exit 9
RESULT=`sha256sum ${FILE:?} | awk '{print $1;}'`

echo "[${RESULT:?}]"
echo "[${STR:?}]"
if [ "${RESULT:?}" != "${STR:?}" ]; then
  exit 1
fi

exit 0

